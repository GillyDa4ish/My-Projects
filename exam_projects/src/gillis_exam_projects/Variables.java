public class Variables {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// declare variables
	      int age;
	      char letter;
	      double touchdown_yardage;
	      boolean answer;

	   // assign values to variables
	      age = 15;
	      letter = 'L';
	      touchdown_yardage = 500;
	      answer = false;

	   // print variable contents to the screen
	      System.out.println(age);
	      System.out.println(letter);
	      System.out.println(touchdown_yardage);
	      System.out.println(answer);
	}

}
